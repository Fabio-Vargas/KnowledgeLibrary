// ## Operadores de Incremento e Decremento
//    **( Operador ) Descrição**

//      ( ++ ) Incremento | let contador = 0; contador++;
let contadorIncremento = 1
contador++
contador++
console.log("Resultado Contador Incremeto" + contadorIncremento)

//      ( ++ ) Decremento | let contador = 0; contador--;
