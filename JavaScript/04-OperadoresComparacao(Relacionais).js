// ## Operadores de Comparação (Relacionais)
//    **( Operador ) Descrição**

//      ( == ) Equal to
let isEqual = (x == y) // Value
let isEqual2 = (x === y) // Value and Format

//      ( != ) Not Equal to
let isNotEqual = (x != y)

//      ( > ) Greater Than
let isGreater = (x > y)

//      ( < ) Less Than
let isLess = (x < y)

//      ( >= ) Greater Than or Equal to
let isGreaterOrEqual = (x >= y)

//      ( <= ) Less Than or Equal to
let isLessOrEqual = (x <= y)
